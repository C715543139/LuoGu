/**
 * Luogu P1719 - 最大加权矩形
 *
 * 问题描述：
 *   给定一个 n × n 的整数矩阵，求所有子矩阵中和最大的那个子矩阵的元素和。
 *   子矩阵：由若干连续行和若干连续列围成的矩形区域。
 *
 * 解法：二维前缀和 + 暴力枚举
 *   1. 边读入边构建二维前缀和 v：
 *        v[i][j] = 矩阵 (1,1) ~ (i,j) 的元素和
 *      递推式：
 *        v[i][j] = a[i][j] + v[i-1][j] + v[i][j-1] - v[i-1][j-1]
 *   2. 枚举所有可能的子矩阵：
 *      - (i, j) 为子矩阵的左上角坐标
 *      - (k, l) 为子矩阵的右下角坐标（i ≤ k, j ≤ l）
 *      - 利用前缀和 O(1) 求出子矩阵的元素和
 *   3. 记录所有子矩阵和的最大值
 *
 * 复杂度分析：
 *   - 时间复杂度：O(n^4)，枚举子矩阵的四个边界
 *   - 空间复杂度：O(n^2)，存储前缀和数组
 *   - 数据规模较小时可接受；若 n 较大，可优化到 O(n^3)
 */

#include <iostream>
#include <climits>
#include <vector>
using namespace std;

int main() {
    int n;
    cin >> n;  // 矩阵大小 n × n
    // v 为二维前缀和数组，多开辟一行一列方便处理边界（1-based）
    vector v(n + 1, vector(n + 1, 0));
    // 边读入边构建前缀和
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            cin >> v[i][j];  // 读入原矩阵元素
            // 递推公式：当前格 = 自身 + 上方前缀和 + 左侧前缀和 - 左上重复部分
            v[i][j] += v[i - 1][j] + v[i][j - 1] - v[i - 1][j - 1];
        }
    }

    // 枚举所有子矩阵，记录最大元素和
    int maxArea = INT_MIN;
    for (int i = 1; i <= n; i++) {          // 左上角行
        for (int j = 1; j <= n; j++) {      // 左上角列
            for (int k = i; k <= n; k++) {  // 右下角行
                for (int l = j; l <= n; l++) {  // 右下角列
                    // 利用前缀和求子矩阵 (i,j) ~ (k,l) 的元素和：
                    //   = 右下前缀 - 上方前缀 - 左侧前缀 + 左上前缀
                    maxArea = max(maxArea, v[k][l] + v[i - 1][j - 1] - v[k][j - 1] - v[i - 1][l]);
                }
            }
        }
    }
    cout << maxArea << endl;
    return 0;
}
